/****** Script for SelectTopNRows command from SSMS  ******/
DECLARE @s nvarchar(200) --= 'B0345E1497050400'
SET @s = N'B0345E1497050400'

SELECT TOP 1000 [TRACE_FILE_HISTORY_ID]
      ,[SRC_SYSTEM]
      ,[PANEL_BARCODE]
      ,[EQUIPMENT_ID]
      ,[MACH_POS]
      ,[LANE_NO]
      ,[STAGE_NO]
      ,[PANEL_EQUIPMENT_ID]
      ,[SETUP_ID]
      ,[PRODUCT_SIDE]
      ,[SRC_FILE_TIMESTAMP]
      ,[SRC_FILE_NAME]
      ,[CIMC_PROCESS_TIMESTAMP]
      ,[CIMC_PROCESS_STATUS]
      ,[TIME_DIFF]
      ,[TRACE_DATA_SOURCE]
      ,[ELINK_QUEUED_TIMESTAMP]
      ,[ELINK_PROCESSED_TIMESTAMP]
      ,[ELINK_EXPORT_TIMESTAMP]
      ,[ELINK_DATA_SOURCE]
      ,[ELINK_PROCESS_STATUS]
      ,[ELINK_XML_FILE]
  FROM [PanaCIM].[dbo].[trace_file_history]
  WHERE PANEL_BARCODE = @s --'B0345E1497050400'--@s



  SELECT TOP 1000 [PANEL_EQUIPMENT_ID]
      ,[EQUIPMENT_ID]
      ,[MASTER_PLACEMENT_ID]
      ,[PANEL_PLACEMENT_ID]
      ,[TIMESTAMP]
      ,[TRX_PRODUCT_ID]
  FROM [PanaCIM].[dbo].[panel_placement_header]
  WHERE PANEL_EQUIPMENT_ID = '3110524' OR PANEL_EQUIPMENT_ID = '3110534' OR PANEL_EQUIPMENT_ID = '3110547' OR PANEL_EQUIPMENT_ID = '3110555'
 
  SELECT TOP 1000 [PANEL_PLACEMENT_ID]
      ,[REEL_ID]
      ,[NC_PLACEMENT_ID]
      ,[PATTERN_NO]
      ,[Z_NUM]
      ,[PU_NUM]
      ,[PART_NO]
      ,[CUSTOM_AREA1]
      ,[CUSTOM_AREA2]
      ,[CUSTOM_AREA3]
      ,[CUSTOM_AREA4]
      ,[REF_DESIGNATOR]
      ,[PATTERN_IDNUM]
      ,[PATTERN_BARCODE]
      ,[PATTERN_DESIGNATOR]
      ,[PREPICKUP_LOT]
      ,[PREPICKUP_STS]
      ,[NCADD]
      ,[NHADD]
      ,[NBLKSERIAL]
      ,[PLACEMENT_ORDER]
      ,[FEEDER_BC]
FROM [PanaCIM].[dbo].[panel_placement_details] 
WHERE PANEL_PLACEMENT_ID = '3085137' OR PANEL_PLACEMENT_ID = '3085147' OR PANEL_PLACEMENT_ID = '3085160' OR PANEL_PLACEMENT_ID = '3085168'